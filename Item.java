public class Item{
// MEMBER VARIABLES
    private String name;
    private double price;

// CONSTRUCTOR
    public Item() {
        this.name = name;
        this.price = price;
    }

    public Item(String name, double price) {
        this.name = name;
        this.price = price;
    }

// GETTERS & SETTERS
    public String getName() {
        return this.name;
    }
    
    public double getPrice() {
        return this.price;
    }
}
